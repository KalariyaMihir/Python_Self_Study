# practice of sort method
# sort method sort the list in ascending and descending order
  
listSort = [ 'hitesh', 'mihir', 'bhagirath']
listSort.sort(reverse=True)

print("Sort method")
print(listSort,'\n')

# Practice of reverse method 
# it reverse the whole 
listReverse = [ 11 , 8 , 2, 6, 66]
listReverse.reverse()

print("reverse method")
print(listReverse , '\n')

# Practice of insert method 
# insert element at the index
